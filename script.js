
window.addEventListener('load', function () {
    var myAudio0 = document.getElementById("myAudio0");
    var playBtn0 = document.getElementById('play0')
    myAudio0.onplaying = function () {
        isPlaying0 = true;
        playBtn0.innerHTML = '<i class="fa fa-pause-circle "></i>';
    };
    myAudio0.onpause = function () {
        isPlaying0 = false;
        playBtn0.innerHTML = '<i class="fa fa-play-circle "></i>';
    };
});

var isPlaying0 = false;

function togglePlay0() {
    if (isPlaying0) {
        myAudio0.pause()
    } else {
        myAudio0.play();
    }
}
